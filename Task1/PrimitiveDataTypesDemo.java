package Task1;
public class PrimitiveDataTypesDemo {
    public static void main(String[] args) {
        
        // Integer types
        byte age = 25; // 1 byte
        short year = 2024; // 2 bytes
        int salary = 50000; // 4 bytes
        long population = 7800000000L; // 8 bytes

        // Floating-point types
        float price = 19.99F; // 4 bytes
        double pi = 3.141592653589793; // 8 bytes

        // Character type
        char grade = 'A'; // 2 bytes

        // Boolean type
        boolean isJavaFun = true; // 1 bit

        // Print values
        System.out.println("Byte Value (Age): " + age);
        System.out.println("Short Value (Year): " + year);
        System.out.println("Int Value (Salary): " + salary);
        System.out.println("Long Value (Population): " + population);
        System.out.println("Float Value (Price): " + price);
        System.out.println("Double Value (PI): " + pi);
        System.out.println("Char Value (Grade): " + grade);
        System.out.println("Boolean Value (Is Java Fun?): " + isJavaFun);
    }
}
